package week1.day3;

public class LearnForLoop {

	public static void main(String[] args) {
		
		for(int i=1;i<=10;i++) {
			
			if(i%2==0) {
				System.out.println(i);
			}
			
		}
		
	       System.out.println("----------------------");
		  
		  //reverseforloop //5 5-1=4 4-1=3 3-1=2 2-1=1 1-1=0 
		  for (int i = 5; i>=0; i--) {
		                               
			  System.out.println(i); 
			  
		  }
		 

	}

}
