package week1.day3;

public class LearnBreakStatement {

	public static void main(String[] args) {
		//Break statement- breaks the loop and comes out of the loop
        //skips the current iteration and executes the remaining
		
		for (int i = 1; i <=10; i++) {
		if(i==7) {
		break;
			}
			
	System.out.println(i);
		}
		
	}

}
